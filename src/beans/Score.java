package beans;

public class Score {
	
	private String homeworkName;
	
	private int score_obtained;
	
	private int maximum_score;

	public String getHomeworkName() {
		return homeworkName;
	}

	public void setHomeworkName(String homeworkName) {
		this.homeworkName = homeworkName;
	}

	public int getScore_obtained() {
		return score_obtained;
	}

	public void setScore_obtained(int score_obtained) {
		this.score_obtained = score_obtained;
	}

	public int getMaximum_score() {
		return maximum_score;
	}

	public void setMaximum_score(int maximum_score) {
		this.maximum_score = maximum_score;
	}

}
